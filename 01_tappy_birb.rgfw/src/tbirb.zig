//! # Tappy Birb
//!
//! A simple demake of Flappy Bird
//!
//! ## Challenge Goals:
//!
//! - [ ] Create a game world with a floor.
//!
//! - [ ] Add an object that represents the main character. Apply a constant force
//!       to the character so it falls to the floor.
//!
//! - [ ] Add obstacles on the right of the game area. The obstacles should slide
//!       across the screen toward the left. The obstacles will appear in
//!       pairs, with a vertical gap between them.
//!
//! - [ ] Detect when the character collides with the floor or obstacles, and reset
//!       the game when a collision occurs.
//!
//! - [ ] Accumulate one point for each obstacle that the player passes. Display
//!       the score.
//!
//! ### Stretch Goals
//!
//! - [ ] Add some sounds that will play each time the player gains a point,
//!       and when the player loses.
//!
//! - [ ] Add a basic game-over screen to display the playerâ€™s score.
//!
//! - [ ] Track the high-score between play sessions and display the high score
//!       alongside the current score.
//!
//! - [ ] Add some background art! Try layering the background and scrolling at
//!       a different rate to the foreground obstacles. This is called Parallax
//!       scrolling.
//!
